package AasicSyntax

fun main() {

    // if ~ else문

    var num1 = 11
    var num2 = 24

    var result : Int

    if (num1 > num2) {
        // 조건식이 true일 때 실행시킬 코드 작성하는 부분
        result = num1

    }else {
        // 조건식이 false일 때 실행시킬 코드 작성하는 부분
        result = num2
    }

    println("큰 수 : $result")

    // 위에 코드를 간략화 시켜보자

}